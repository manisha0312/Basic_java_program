
public class IncrementDecrement {
	public static void main(String args[]) {
		int x=10;
		System.out.println(x++);// after x=x+1;
		System.out.println(++x);// first x=x+1
		int x1=10;
		System.out.println(x1);
		++x1;
		System.out.println(x1);
		int x2=10;
		System.out.println(x2);
		x2++;
		System.out.println(x2);
		int w=22;
		int s=w++;
		System.out.println(w);// after x=x+1;
		System.out.println(s);// first x=x+1
		 s=++w;
		System.out.println(w);// after x=x+1;
		System.out.println(s);// first x=x+1 
	}

}
